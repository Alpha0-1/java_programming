/**
 * 10-escape_sequences.java
 * Purpose: Demonstrates common escape sequences in Java strings
 */
public class EscapeSequences {
    public static void main(String[] args) {
        // Newline and tab characters
        System.out.println("Hello,\nWorld!"); // \n for newline
        System.out.println("Name:\tJohn Doe"); // \t for tab

        // Quotation marks
        System.out.println("He said, \"Java is fun!\"");

        // Backslash
        System.out.println("Path: C:\\Program Files\\Java");
    }
}
